class Solution {
  public:
    int distributeTicket(int N, int K) {
        // code here
         int s=1;
       int e = N;
       while(e>s){
           s=s+K;
          
            if(s>e){
                return e;
            }
             e=e-K;
             if(s>e){
                 return s;
        }    }
    }
};


