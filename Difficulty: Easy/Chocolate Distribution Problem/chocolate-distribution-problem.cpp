class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
        sort(a.begin(),a.end());
        int p1=0;
        int p2 = m-1;
        vector<int> v;
        while(p2!=n)
        {
            int h = a[p2] - a[p1];
            v.push_back(h);
            p1++;
            p2++;
        }
        sort(v.begin(),v.end());
        return v[0];
    }   
};

